from __future__ import annotations

from aurora_agent_core.llm.zai_client import ZaiChatResult
from aurora_agent_core.miners.dataset_miner_graph import DatasetMinerGraph


class FakeResponse:
    def __init__(self, payload):
        self.payload = payload

    def raise_for_status(self) -> None:
        return None

    def json(self):
        return self.payload


class FakeClient:
    def __init__(self, *args, **kwargs):
        pass

    def __enter__(self):
        return self

    def __exit__(self, *args):
        return False

    def post(self, url, json):
        return FakeResponse(
            {
                "vulns": [
                    {
                        "id": "GHSA-test-0001",
                        "summary": "Test Web3 vulnerability",
                        "details": "A real OSV-style vulnerability record.",
                        "aliases": ["CVE-2099-0001"],
                        "published": "2099-01-01T00:00:00Z",
                        "modified": "2099-01-02T00:00:00Z",
                        "database_specific": {
                            "severity": "HIGH",
                            "cwe_ids": ["CWE-20"],
                        },
                        "references": [
                            {"type": "ADVISORY", "url": "https://example.com/advisory"},
                        ],
                    }
                ]
            }
        )


def test_dataset_miner_uses_osv_records(monkeypatch, tmp_path) -> None:
    monkeypatch.setattr("aurora_agent_core.miners.dataset_miner_graph.httpx.Client", FakeClient)
    result = DatasetMinerGraph().run(
        {
            "task_id": "task_osv_test",
            "task_type": "dataset_generation",
            "title": "Web3 漏洞数据集",
            "goal": "构建 Web3 漏洞数据集",
            "requirements": ["仅真实公开来源"],
            "constraints": ["仅公开来源"],
            "output_format": "jsonl",
            "target_size": 1,
            "source_scope": ["osv"],
        },
        output_dir=tmp_path / "dataset",
    )
    assert result["status"] == "completed"
    assert result["summary"]["records"] == 1
    dataset_text = (tmp_path / "dataset" / "dataset.jsonl").read_text(encoding="utf-8")
    assert "GHSA-test-0001" in dataset_text
    assert '"synthetic_mvp": false' in dataset_text


