def test_dataset_miner_uses_zai_for_synthetic_records_and_report(monkeypatch, tmp_path) -> None:
    calls = []

    def fake_llm(messages, *args, **kwargs):
        calls.append(messages)
        system = messages[0]["content"]
        if "report writer" in system:
            return ZaiChatResult(
                content="# Dataset Report\n\n- Generated by mocked Z.ai.",
                usage={
                    "provider": "zai",
                    "model": "glm-test",
                    "base_url": "https://example.test",
                    "prompt_tokens": 30,
                    "completion_tokens": 10,
                    "total_tokens": 40,
                },
            )
        return ZaiChatResult(
            content='[{"title":"Reentrancy synthetic case","category":"reentrancy","prompt":"Analyze the callback risk.","answer":"The state update happens after an external call.","reasoning":"This matches a reentrancy pattern.","source_url":"https://github.com/search?q=reentrancy"}]',
            usage={
                "provider": "zai",
                "model": "glm-test",
                "base_url": "https://example.test",
                "prompt_tokens": 50,
                "completion_tokens": 20,
                "total_tokens": 70,
            },
        )

    monkeypatch.setattr("aurora_agent_core.miners.dataset_miner_graph.call_zai_chat_with_usage", fake_llm)

    result = DatasetMinerGraph().run(
        {
            "task_id": "task_dataset_llm_test",
            "task_type": "dataset_generation",
            "metadata": {"use_llm": True},
            "title": "Web3 漏洞数据集",
            "goal": "构建 1 条 Web3 漏洞 synthetic 数据集",
            "requirements": ["覆盖重入"],
            "constraints": ["仅公开来源"],
            "output_format": "jsonl",
            "target_size": 1,
            "source_scope": ["github"],
        },
        output_dir=tmp_path / "dataset_llm",
    )

    assert len(calls) == 2
    assert result["status"] == "completed"
    assert result["summary"]["records"] == 1
    assert result["usage"]["extraction_method"] == "zai_synthetic_generation"
    assert result["usage"]["llm"]["calls"] == 2
    assert result["usage"]["llm_total_tokens"] == 110
    dataset_text = (tmp_path / "dataset_llm" / "dataset.jsonl").read_text(encoding="utf-8")
    assert '"model_generated": true' in dataset_text
    assert "Reentrancy synthetic case" in dataset_text
